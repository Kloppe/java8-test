package com.java.xingfu.common.constants;

/**
 * @author 掘金
 * @date 2018/12/29
 * @desc
 */
public class TimeConstant {
    /**
     * 每秒的毫秒数
     */
    public final static long MILLISECONDS_PER_SECOND = 1000L;

    /**
     * 每分钟的毫秒数
     */
    public final static long MILLISECONDS_PER_MINUTE = 60L * MILLISECONDS_PER_SECOND;

    /**
     * 每小时的毫秒数
     */
    public final static long MILLISECONDS_PER_HOUR = 60L * MILLISECONDS_PER_MINUTE;

    /**
     * 每天的毫秒数
     */
    public final static long MILLISECONDS_PER_DAY = 24L * MILLISECONDS_PER_HOUR;

    /**
     * 每分钟的秒数
     */
    public final static long SECONDS_PER_MINUTE = 60L;

    /**
     * 每小时的秒数
     */
    public final static long SECONDS_PER_HOUR = 60L * SECONDS_PER_MINUTE;

    /**
     * 每天的秒数
     */
    public final static long SECONDS_PER_DAY = 24L * SECONDS_PER_HOUR;
}
